export function findDestination(input: string): Destination | null {
  const q = (input || '').toLowerCase().trim();
  if (!q) return null;
  return DESTINATIONS.find((d) =>
    d.code.toLowerCase() === q ||
    d.en.toLowerCase() === q ||
    d.zh.toLowerCase() === q
  ) || null;
}

export function resolveRoute(destination: Destination | null, override: 'auto' | 'hague' | 'non'): 'hague' | 'non' | null {
  if (!destination) return null;
  if (override === 'hague') return 'hague';
  if (override === 'non') return 'non';
  return destination.hague ? 'hague' : 'non';
}

export function routeLabel(locale: 'en' | 'zh', route: 'hague' | 'non' | null): string {
  if (!route) return '-';
  if (locale === 'zh') return route === 'hague' ? '海牙（Apostille）' : '非海牙（认证+领馆）';
  return route === 'hague' ? 'Hague (Apostille)' : 'Non-Hague (Authentication + Consular)';
}
